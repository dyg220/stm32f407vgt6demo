#include "fire.h"

/*
*************************************************************
*函数功能：火焰传感器初始化
*参    数：None
*返 回 值：None
*备    注：DO---PA4
*************************************************************
*/
void Fire_Init(void)
{
	RCC->AHB1ENR |= (1 << 0);

	//配置输入模式
	GPIOA->MODER &= ~(0x3 << 8);
	GPIOA->PUPDR &= ~(0x3 << 8);
}

